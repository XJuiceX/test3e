

$('input').on('click', function () {
    if ( $(this).is(':checked') ) {
        $("label[for='" + this.id + "']").addClass("myClass ");
    } else {
        $("label[for='" + this.id + "']").removeClass("myClass ");
    }
});



$('label input').on("click", function() {

    if($(".oneChoose1 input:checked").length >= 1) { 
        
       $(".oneChoose1 input:not(:checked)").attr("disabled", true);
    
    } else {
        
       $(".oneChoose1 input:disabled").attr("disabled", false);
    
    }

});


